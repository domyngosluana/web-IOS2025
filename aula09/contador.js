// Contar de 0 a 10
let inicioDaContagem = 2
let contador = inicioDaContagem

// While = enquanto
// O  computador repetirá o código dentro {} enquanto a condição for verdadeira. No nosso, o contador vai até o limite 100, se passar disso ele para, porque 101 é maior que 100, portanto contador <= 100 é falso.
while (contador <= 100) {
    console.log(contador);
    contador = contador + 2
}

